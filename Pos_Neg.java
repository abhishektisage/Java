import java.util.*;
public class Pos_Neg{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);

        int num=sc.nextInt();

        if(num>0){
            System.out.println("the num is positive");
        }
        else if(num<0){
            System.out.println("the num is negative");
        }
            
        
        else{
            System.out.println("it is a zero");
        }

    }
    
}
